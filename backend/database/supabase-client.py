# Steps to merge a branch in Git:
# 1. Fetch the latest changes:
#    git fetch origin
# 2. Checkout the branch you want to merge into (e.g., main):
#    git checkout main
# 3. Merge the branch (e.g., feature-branch) into the current branch:
#    git merge feature-branch
# 4. Resolve any conflicts if they arise.
# 5. Commit the merge if necessary:
#    git commit -m "Merge feature-branch into main"
# 6. Push the changes to the remote repository:
#    git push origin main
